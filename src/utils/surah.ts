/**
 * Returns the Arabic Surah name for a given ID (1-114).
 */
export function getSurahNameArabic(id: number): string {
  return SURAH_NAMES_ARABIC[id - 1] || "";
}

/**
 * Identifies the active Surah number for a page by inspecting the first valid verse.
 */
export function getSurahForPage(lines: MushafLine[]): number {
  for (const line of lines) {
    if (line.words && line.words.length > 0) {
      const firstWord = line.words.find(w => w.s);
      if (firstWord) {
        const surah = parseInt(firstWord.s, 10);
        if (!isNaN(surah)) return surah;
      }
    }
  }
  return 1;
}
